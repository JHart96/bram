## ----setup--------------------------------------------------------------------
library(bram)
library(brms)

## -----------------------------------------------------------------------------
model <- bram(cyl ~ scale(hp) + scale(disp), data=mtcars, family="poisson")

## -----------------------------------------------------------------------------
model$get_priors()

## -----------------------------------------------------------------------------
priors <- c(
  prior(normal(0, 0.5), coef="scalehp"),
  prior(normal(0, 0.8), coef="scaledisp"),
  prior(normal(1, 1), class="Intercept")
)
model$set_priors(priors)
model$prior_check()

## -----------------------------------------------------------------------------
model$fit()

## -----------------------------------------------------------------------------
model$diagnostics()

## -----------------------------------------------------------------------------
model$summary()

